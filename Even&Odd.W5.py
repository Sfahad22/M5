# This programs finds whether the number is Even or Odd.

random_numbers = [12, 1, 34, 45, 56, 67, 78, 89, 90, 99]

for number in random_numbers:
     if number % 2 == 0:
        print(f"Hey - {number} is even.")
     else:
        print(f"Gosh, {number} is odd.")
